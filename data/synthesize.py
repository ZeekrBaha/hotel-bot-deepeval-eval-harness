def generate_cases(n: int = 1000, seed: int = 0) -> list[dict]:  # noqa: ARG001
    """Return n synthetic cases. Deterministic given seed (variation is index-based)."""
    cases: list[dict] = []
    n_kinds = len(KINDS)
    n_langs = len(LANGS)

    for i in range(n):
        # Cycle through kinds and languages based on index
        kind = KINDS[i % n_kinds]
        lang = LANGS[(i // n_kinds) % n_langs]

        ru_pool, ky_pool, builder = _KIND_BUILDERS[kind]

        if builder is not None:
            partial = builder(lang, i)
        else:
            # _KIND_BUILDERS invariant: kinds without a builder always carry pools.
            assert ru_pool is not None and ky_pool is not None
            pool = ru_pool if lang == "ru" else ky_pool
            partial = _simple_case(pool, lang, i)

        case_id = f"synth-{kind}-{i}"
        case = {
            "id": case_id,
            "kind": kind,
            "lang": lang,
            **partial,
        }
        cases.append(case)

    return cases


def write_jsonl(cases: list[dict], path: str) -> None:
    """Write one JSON object per line, ensure_ascii=False."""
    out = Path(path)
    out.parent.mkdir(parents=True, exist_ok=True)
    with out.open("w", encoding="utf-8") as f:
        for case in cases:
            f.write(json.dumps(case, ensure_ascii=False) + "\n")


def main(n: int = 1000, out: str = "data/goldens_synth.jsonl") -> None:
    write_jsonl(generate_cases(n), out)
    print(f"wrote {n} cases -> {out}")


if __name__ == "__main__":
    main(int(sys.argv[1]) if len(sys.argv) > 1 else 1000)
