def _pick(pool: list, idx: int) -> str:
    """Cycle through pool deterministically by index."""
    return pool[idx % len(pool)]


def _booking_complete_case(lang: str, i: int) -> dict:
    date_set = BOOK_DATES[i % len(BOOK_DATES)]
    n_guests = BOOK_GUEST_COUNTS[i % len(BOOK_GUEST_COUNTS)]

    if lang == "ru":
        name = _pick(BOOK_COMPLETE_RU_NAMES, i)
        start_msg = _pick(BOOK_COMPLETE_RU_STARTS, i)
        detail_tpl = _pick(BOOK_COMPLETE_RU_DETAILS, i)
        cin_str, cout_str = date_set[0], date_set[1]
        detail_msg = detail_tpl.format(name=name, cin=cin_str, cout=cout_str, n=n_guests)
        messages = [
            {"role": "user", "content": start_msg},
            {"role": "assistant", "content": "Как вас зовут?"},
            {"role": "user", "content": detail_msg},
        ]
    else:
        name = _pick(BOOK_COMPLETE_KY_NAMES, i)
        start_msg = _pick(BOOK_COMPLETE_KY_STARTS, i)
        detail_tpl = _pick(BOOK_COMPLETE_KY_DETAILS, i)
        cin_str, cout_str = date_set[2], date_set[3]
        detail_msg = detail_tpl.format(name=name, cin=cin_str, cout=cout_str, n=n_guests)
        messages = [
            {"role": "user", "content": start_msg},
            {"role": "assistant", "content": "Атыңыз ким?"},
            {"role": "user", "content": detail_msg},
        ]

    return {
        "messages": messages,
        "expected": {
            "source": "synthetic",
            "expected_slots": {"guest_name": name, "num_guests": n_guests},
            "should_confirm": True,
        },
    }


def _booking_incomplete_case(lang: str, i: int) -> dict:
    date_set = BOOK_DATES[i % len(BOOK_DATES)]
    n_guests = BOOK_GUEST_COUNTS[i % len(BOOK_GUEST_COUNTS)]

    if lang == "ru":
        name = _pick(BOOK_COMPLETE_RU_NAMES, i)
        tpl = _pick(BOOK_INCOMPLETE_RU, i)
        content = tpl.format(
            name=name,
            cin=date_set[0],
            cout=date_set[1],
            n=n_guests,
        )
    else:
        name = _pick(BOOK_COMPLETE_KY_NAMES, i)
        tpl = _pick(BOOK_INCOMPLETE_KY, i)
        content = tpl.format(
            name=name,
            cin=date_set[2],
            cout=date_set[3],
            n=n_guests,
        )

    return {
        "messages": [{"role": "user", "content": content}],
        "expected": {
            "source": "synthetic",
            "should_confirm": False,
        },
    }


def _simple_case(pool: list[str], lang: str, i: int) -> dict:
    content = _pick(pool, i)
    return {
        "messages": [{"role": "user", "content": content}],
        "expected": {"source": "synthetic"},
    }


